    private void btnConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmActionPerformed
        String maOTP = txOTP.getText();
        if(maOTP.equals(otp)){
            JOptionPane.showMessageDialog(null, "Mã xác nhận hợp lệ !");
            JXacThuc.setVisible(false);
            DoiMatKhau.setVisible(true);
        }else{
            JOptionPane.showMessageDialog(null, "Mã xác nhận không hợp lệ !");
        }
        
    }//GEN-LAST:event_btnConfirmActionPerformed

    private void btnXacNhanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXacNhanActionPerformed
        boolean check = true;
        String matkhau = String.valueOf(txtMatkhauMoi.getPassword());
        String re_matkhau = String.valueOf(txtMatkhauMoi.getPassword());

        if(Validation.isEmpty(matkhau) || Validation.isEmpty(re_matkhau)){
            JOptionPane.showMessageDialog(null,"Vui lòng điền đầy đủ thông tin !");
        }else{
            if(!matkhau.equals(re_matkhau)){
                JOptionPane.showMessageDialog(null,"Mật khẩu nhập lại không trùng khớp !");
                check = false;
            }

            if(check){
                QuanNhan quanNhan = new QuanNhan(email, matkhau);
                if(!qndao.capNhatMatKhau(quanNhan)){
                    JOptionPane.showMessageDialog(btnConfirm, "Thay đổi mật khẩu thành công");
                    dispose();
                    new Login_Form().setVisible(true);
                }else{
                    JOptionPane.showMessageDialog(btnConfirm, "Thay đổi mật khẩu thất bại");
                }
            }
        }
    }//GEN-LAST:event_btnXacNhanActionPerformed

